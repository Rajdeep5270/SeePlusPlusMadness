#include <iostream>
using namespace std;

main() {

    int a = 12, b = 6;
    cout << "Addition of " << a << " + " << b << " = " << a + b << endl;
    cout << "Subtraction of " << a << " - " << b << " = " << a - b << endl;
    cout << "Multiplication of " << a << " * " << b << " = " << a * b << endl;
    cout << "Division of " << a << " / " << b << " = " << a / b << endl;
    cout << "Modulo of " << a << " % " << b << " = " << a % b << endl;

}